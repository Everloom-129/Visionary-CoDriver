    if args.path.endswith(".mp4"):
        raise ValueError('The input video should be a directory, not a file')
    else:
        infer_list = sorted((os.listdir(args.path)))

    from tqdm import tqdm
    for v_name in tqdm(infer_list):
        v_path = os.path.join(args.path, v_name)
        cap = cv2.VideoCapture(v_path if args.demo == "video" else args.camid)
        width = cap.get(cv2.CAP_PROP_FRAME_WIDTH)  # float
        height = cap.get(cv2.CAP_PROP_FRAME_HEIGHT)  # float
